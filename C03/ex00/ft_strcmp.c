int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	res;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
	{
		i++;
	}
	res = s1[i] - s2[i];
	if (res > 0)
		return (1);
	if (res < 0)
		return (-1);
	return (0);
}
