int	ft_str_is_uppercase(char *str)
{
	int	is_uc;
	int	i;

	is_uc = 1;
	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
			is_uc = 0;
		i++;
	}
	return (is_uc);
}
